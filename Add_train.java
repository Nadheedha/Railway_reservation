    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        DefaultTableModel d = (DefaultTableModel)jTable3.getModel();
        int selectIndex = jTable3.getSelectedRow();
        t_no.setText(d.getValueAt(selectIndex, 0).toString());
        t_name.setText(d.getValueAt(selectIndex, 1).toString());
        Date date,date1;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String)d.getValueAt(selectIndex, 2).toString());
            start_date1.setDate(date);
            date1 = new SimpleDateFormat("yyyy-MM-dd").parse((String)d.getValueAt(selectIndex, 3).toString());
            destiny_date1.setDate(date1);
        } catch (ParseException ex) {
            Logger.getLogger(Add_train.class.getName()).log(Level.SEVERE, null, ex);
        }
        d_time.setText(d.getValueAt(selectIndex, 4).toString());
        a_time.setText(d.getValueAt(selectIndex, 5).toString());
        d_place.setText(d.getValueAt(selectIndex, 6).toString());
        a_place.setText(d.getValueAt(selectIndex, 7).toString());
        price.setText(d.getValueAt(selectIndex, 8).toString());
        seats_avail.setText(d.getValueAt(selectIndex, 9).toString());
        
        ADD.setEnabled(false);
    }//GEN-LAST:event_jTable3MouseClicked

