    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        String tno=t_no.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/railway", "root", "");
            String sql="DELETE FROM `train` WHERE `tno`=?";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1,tno);

            if(pst.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(null,"DETAILS DELETED SUCCESSFULLY");
            }
            else{
                JOptionPane.showMessageDialog(null,"DELETION FAILED");
            }
            Train_Details();
            reset();
            ADD.setEnabled(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"TRAIN DETAILS IS NOT updated SUCCESSFULLY");
            JOptionPane.showMessageDialog(null, e);
        }  
       
    }//GEN-LAST:event_deleteActionPerformed

